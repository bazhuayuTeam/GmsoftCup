package com.cqut.util;

/**
 * 服务端用户登录注销接口
 * 允许用户登录、注销、可以获取当前登录用户。
 * @author 袁庭飞
 *
 */
public interface OperatorUtil {
	
	/**
	 * 存在会话中的登录用户的属性名称，例如
	 * <code>
	 * 		getSession().getAttribute(ViewService.LOGIN_USER, loginUser);
	 * </code>
	 */
	public final static String LOGIN_USER = "loginUser";
	
	/**
	 * 存在会话中的登录用户所属操作员的ID的属性名称，例如
	 * <code>
	 * 		getSession().getAttribute(ViewService.OPERATOR_ID, loginUser);
	 * </code>
	 */
	public final static String OPERATOR_ID = "operatorId";
	
	/** 用户名字符常量 */
	public final static String OPERATOR_NAME= "operatorName";
	/** 用户名角色Id*/
	public final static String ROLE_ID= "roleID";
	
	/** 用户密码字符常量 */
	public final static String PASSWORD = "password";
	
	/** 用户类型 */
	public static final String OPERATOR_TYPE = "operatorKind";
	/** 职工号 */
	public static final String ZGH = "ZGH";
	/** 学院ID */
	public static final String ACADEMICID = "academicID";
	/** 专业ID */
	public static final String DISCIPLINEID = "disciplineID";
	/** 专业Code */
	public static final String MAJORCODE = "majorCode";
	
}